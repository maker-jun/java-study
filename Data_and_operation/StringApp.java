
public class StringApp {
	
	public static void main(String[] args) {
		
		System.out.println("1 Hello World"); //String 문자열
		System.out.println('2');//Character 문자
		System.out.println("3 H");
		
		System.out.println("4 Hello"
				+ " World");
		System.out.println("5 Hello \nWorld");
		System.out.println("6 Hello \"World\"");
	}

}
